import UIKit

extension UIColor{
    
    static func backgroundColor() -> UIColor{
        
        return UIColor(red: 215.0/255, green: 212.0/255, blue: 200.0/255, alpha: 1)
        
    }
    
    static func specialOfferColor() -> UIColor{
        
        return UIColor(red: 146.0/255, green: 15.0/255, blue: 0, alpha: 1)
        
    }
    
}